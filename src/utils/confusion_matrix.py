"""
find the confusion matrix for the model trained and evaluated on the 
test set
"""

import numpy as np
import tensorflow as tf


def plot_confusion_matrix(test_labels_set, test_blend_set, test_index_set, predictions):
    """
    plots the confusion matrix for the test set vs the
    predictions generated by the trained model

    Args:
        test_labels_set (list): list of the test set labels
        test_blend_set (list): list of the blendshapes test set
        test_index_set (list): list of the indexed test set
        predictions (list): list of the predictions generated by the trained model

    """
    errors_count = {"0": 0, "1": 0, "2": 0}
    ground_truth = []
    errors = []

    for i in test_labels_set:
        i = np.argmax(i)
        ground_truth.append(i)
    for j in range(len(test_blend_set)):
        if predictions[j] != ground_truth[j]:     # check if the predicted label for the image is equal to the ground truth label.
            errors.append(test_index_set[j])
    confusion_matrix = tf.math.confusion_matrix(      # create the confusion matrix
        ground_truth,
        predictions,
        num_classes=3,
        dtype=tf.dtypes.int32,
    )

