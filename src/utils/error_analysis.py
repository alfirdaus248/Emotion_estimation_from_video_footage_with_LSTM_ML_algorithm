"""visualize misclassified images from the test set"""

import csv
import numpy as np
import cv2
import matplotlib.pyplot as plt
import mediapipe as mp
from mediapipe_tools.visualizing_and_setup import detector, draw_landmarks_on_image
from utils.csv_writer import csv_writer
from utils.prediction import generate_prediction


def count_visualize_errors(x_test):
    """
    counts the error by comparing the predictions to the
    test set ground truth labels and visualize the undetected images

    Args:
        predictions (list): list of predictions generated by the trained model

    """

    predictions = generate_prediction(x_test)
    ground_truth = []
    errors = []
    test_blend_set = []
    test_index_set = []

    with open("test_set_full_index.csv", mode="r", encoding="utf-8") as test_data:   # open the csv file
        csvfile = csv.reader(test_data)
        next(csvfile)
        for lines in csvfile:
            test_blend_set.append(lines[0:52])

        for j in range(len(test_blend_set)):
            if predictions[j] != ground_truth[j]:        # compare the predicted value of the label to the ground truth value
                errors.append(test_index_set[j])       # create a list with the indecies of the mispredicted images

        for ind in errors:
            imageee = (
                np.array(str(test_blend_set[int(float(ind))][1]).split(" "))
                .reshape(48, 48, 1)
                .astype(np.uint8)
            )
            imageees = cv2.cvtColor(imageee, cv2.COLOR_GRAY2RGB)
            frame = mp.Image(image_format=mp.ImageFormat.SRGB, data=imageees)
            detection_result = detector()
            detection_result = detection_result.detect(frame)
            annotated_image = draw_landmarks_on_image(         # draw landmarks on the image
                frame.numpy_view(), detection_result
            )
            plt.imshow(annotated_image)
            plt.show()
            plt.imshow(imageees)
            plt.show()


def create_classes_corrcoef(traindata):
    """
    creates three lists contains the images of each class,
    create files for them and visualize the correlation matrix
    besides other visualizations for the blendshapes

    Args:
        traindata (list): list of the training set instances

    """

    class1 = []
    class2 = []
    class3 = []

    for ins in traindata:       # create lists for the dataset classes 
        if float(ins[52]) == 0:
            class1.append(ins)
        elif float(ins[52]) == 1:
            class2.append(ins)
        elif float(ins[52]) == 2:
            class3.append(ins)

    csv_writer("class1.csv", "beedoo", class1)
    csv_writer("class2.csv", "beedoo", class2)
    csv_writer("class3.csv", "beedoo", class3)

    plt.figure(figsize=[40, 40])
    y = np.linspace(0, 1999, 1999)    # generate the y-axis values to be ploted against the class values on the x-axis
    for j in range(1, 52):
        plt.subplot(10, 6, j)         # create subplots for the three classes
        class1_slice = [float(i[j]) for i in class1[1:2000]]
        class2_slice = [float(i[j]) for i in class2[1:2000]]
        class3_slice = [float(i[j]) for i in class3[1:2000]]
        plt.title(f"{j}")
        plt.scatter(y, class1_slice, color="blue")
    plt.scatter(y, class2_slice, color="red")
    plt.scatter(y, class3_slice, color="green")
    print(class1[1:100])
    corr_mat = np.corrcoef(class1_slice, class2_slice)     # calculate the correlation coefficient
    print(corr_mat)
